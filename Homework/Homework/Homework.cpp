#include <iostream>
using namespace std;
int main()
{
    cout << "Ilk sayiyi giriniz : ";
    int firstNumber;
    cin >> firstNumber;

    cout << "Ikinci sayiyi giriniz : ";
    int secondNumber;
    cin >> secondNumber;

    cout << "Iki sayinin toplami : " << firstNumber + secondNumber << endl;
    cout << "Iki sayinin farki : " << firstNumber - secondNumber << endl;
    cout << "Iki sayinin carpimi : " << firstNumber * secondNumber << endl;
    cout << "Iki sayinin bolumu : " << firstNumber / secondNumber << endl;



}
